     <div class="modal-header">
	  <h4>Landslides</h4>
		 <button type="button" class="close" data-dismiss="modal" aria-hidden="true">×</button>
		
      </div>
      <div class="modal-body">
      
		<div class="section-title text-center">

      <p><b>Way to protect your house and family from landslide:</b></p>
           
			<p>- Know the history of the area before building house and gardens<br>
				- Don’t cut down too much trees in sloppy areas or hill<br>
				- Plant some trees in landslide prone areas to hold the ground together<br>
				- Don’t build house at the bottom of the hill or cliff<br>
				- If you live in a landslide prone area identify safe place</p>
				
			<p><b>During a severe storm, stay alert and awake. Many deaths <br>
			from landslides occur while people are sleeping.<p></b>
			
			<b>1.</b> Listen to local news stations on a battery-powered<br>
			 radio for warnings of heavy rainfall.<br>
			<b>2.</b> Listen for unusual sounds that might indicate moving debris, <br>
			such as trees cracking or boulders knocking together.<br>
			<b>3.</b> Move away from the path of a landslide or debris flow as quickly<br>
			 as possible. The danger from a mudflow increases near stream<br> 
			channels and with prolonged heavy rains. Mudflows can move <br>
			faster than you can walk or run. Look upstream before crossing <br>
			a bridge and do not cross the bridge if a mudflow is approaching.<br>
			<b>4.</b> Avoid river valleys and low-lying areas.<br>
			<b>5.</b> If you are near a stream or channel, be alert for any sudden increase or decrease in water flow and notice whether the water changes from clear to muddy. Such changes may mean there is debris flow activity upstream so be prepared to move quickly.
			Curl into a tight ball and protect your head if escape is not possible.		
			
