		 <button type="button" class="close" data-dismiss="modal" aria-hidden="true">×</button>
		
      </div>
      <div class="modal-body">
      
		<div class="section-title text-center">

      <p>Tsunami is a series of waves and the first one might not be the biggest</p>
            <p>Wave may come within minutes, don’t wait for official warning move to higher ground</p>
			<p>Following an earthquake (either big or long) immediately move to higher ground on foot and wait until the all clear is given by NDMO before coming down. Remember to take emergency kit as the all clear can take more than 2hrs</p>
			<p>Natural signs that might bring a tsunami:<br>
				- Strong earthquake<br>
				- Long earthquake (lasting longer than 60 seconds)<br>
				- Loud noise coming from the ocean<br>
				- Draw back of the ocean</p>
			<p><b>IF YOU ARE UNDER A TSUNAMI WARNING:</b></p>
			<p><b>1.</b> First, protect yourself from an Earthquake. Drop, Cover,<br> 
			then Hold On.</p>
			<p><b>2.</b> Get to high ground as far inland as possible.</p>
			<p><b>3.</b> Be alert to signs of a tsunami, such as a sudden <br> rise or draining of 
			ocean waters.</p>
			<p><b>4.</b> Listen to emergency information and alerts.</p>
			<p><b>5.</b> Evacuate: DO NOT wait! Leave as soon as you see any natural signs of a tsunami 
			or receive an official tsunami warning.</p>
			<p><b>6.</b> If you are in a boat, go out to sea.</p>
			
