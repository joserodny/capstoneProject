		<div class="section-title text-center">
			<p>A Volcanic Eruption can be an awesome and destructive event.</p>	
        <p>Community to be aware of volcanic activities and wear protective clothes, long sleeve, mask, glasses, scarf or hat.</p>
			<p>During times of increased activity you should store water and food in closed or shelters containers and food must be washed before cooking.</p>
			<p>During increased ash fall the community needs to beware that mudslides can occur during heavy rain.</p>
			<p>Know your volcano hazard map and danger zones</p>
			<p>Know where to get volcano bulletins and alert levels-Radio Vanuatu (National Emergency broadcast)</p>
			<p><b>SAFETY TIPS</b></p>
			<div class="text-center">
			<p><b>•</b> As much as possible, stay away from active volcanoes.</p>
			<p><b>•</b> If you live near an active volcano, keep goggles and a mask in an emergency
			kit, along with a flashlight and a working, battery-operated radio.</p>
			<p><b>•</b> Know your evacuation route. Keep gas in your car.</p>
			<p><b>If a Volcano Erupts in Your Area</b></p>
			<p><b>•</b> Evacuate only as recommended by authorities to stay clear of lava, 
			mud flows, and flying rocks and debris.</p>
			<p><b>•</b> Avoid river areas and low-lying regions.</p>
			<p><b>•</b> Before you leave the house, change into long-sleeved shirts and 
			long pants and use goggles or eyeglasses, not contacts. Wear an emergency mask 
			or hold a damp cloth over your face.</p>
			<p><b>•</b> If you are not evacuating, close windows and doors and block 
			chimneys and other vents, to prevent ash from coming into the house.</p>
			<p><b>•</b> Be aware that ash may put excess weight on your roof and need to be swept away. Wear protection during cleanups.</p>
			<p><b>•</b> Ash can damage engines and metal parts, so avoid driving. If you must drive, stay below 35 miles (56 kilometers) an hour.
			</p>
