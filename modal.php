      <p><b>Ways to protect your house, garden and family from flooding:</b></p>
           
			<p>Build steal or floor house in low lying areas<br>
				- During the design  of the house  and garden look to include drainage system<br>
				- Look to include water catchment systems<br>
				- If your house is prone to flooding think about rebuilding to higher ground<br>
				- When river is high do not cross<br>
				- Don’t let children play in flood waters</p>	
				<p><b>During the Flood</b></p>

				<b>1.</b> Avoid areas subject to sudden flooding.<br>
				<b>2.</b> Do not attempt to cross rivers of flowings<br>
				streams where water is above the knee.<br>
				<b>3.</b> Beware of water-covered roads and bridges.<br>
				<b>4.</b> Avoid unnecessary exposure to the elements.<br>
				<b>5.</b> Do not go swimming or boating in swollen rivers.<br>
				<b>6.</b> Eat only well-cooked food. Protect <br>
				left-overs against contamination.<br>
				<b>7.</b> Drink clean or preferably boiled water ONLY.
			
