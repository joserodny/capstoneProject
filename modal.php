		 <button type="button" class="close" data-dismiss="modal" aria-hidden="true">×</button>
		
      </div>
      <div class="modal-body">
      
		<div class="section-title text-center">

      <p>Not all earthquakes cause tsunami but if you feel a strong earthquake or a long one you should move to higher ground</p>
           
			<p><b>STOP, DROP, HOLD & COVER</b></p>
			<p><b>Survival Tips</b></p>
		<p><b>1.</b> Get away from glass, large furniture, and other hazards, <br>
		if possible.<br> 
		<b>•</b> In the first few seconds after the shaking starts, <br>try to quickly move 
		away from anything<br> that could fall and injure you.</p>

		<p><b>2.</b> Drop to your hands and knees under a sturdy table or desk.<br>
		<b>•</b> Look for a sturdy piece of furniture, such as a solid table, <br>
		that can offer cover from falling objects. Get down on your<br> 
		hands and knees, and crouch under the desk or table<br>
		until the shaking stops.</p>

		<p><b>3.</b> Protect your head and neck from falling debris.<br>
		<b>•</b> If possible, grab a pillow, sofa cushion, or another	object to <br> 
		shield your face and head. If there’s nothing nearby to use<br> 
		as a shield, cover your face, head, and neck <br>with your hands and arms.</p>

		<p><b>4.</b> Remain in your safe spot until the shaking stops.<br>
		<b>•</b> Stay put until the shaking has stopped for 1 or 2 minutes.<br>
		Remain on guard when you get up, as aftershocks<br> 
		can occur at any time after an earthquake.</p>
			
		</div>
